<p>ChineseDict relies on the following datasets:</p>

<ul class="list-disc list-inside ml-4">
    <li class="">English definitions from <a href="https://cc-cedict.org/wiki/">CC-CEDICT</a>.</li>
    <li class="">HSK levels from <a href="https://www.chinesetest.cn/ChangeLan.do">chinesetest.cn</a>, compiled by <a href="https://www.chinees-leren.nl/dictionary/">chinees-leren.nl</a>.</li>
    <li class="">Examples from <a href="https://tatoeba.org/">Tatoeba</a>.</li>
</ul>

<p>The above data sources are compiled along with manually updated information
into a downloadable merged Chinese dictionary dataset (around 50MB), which is available 
<a href="https://github.com/jeffcarp/chinesedict/blob/main/data/dictionary.textproto">here</a>.
</p>